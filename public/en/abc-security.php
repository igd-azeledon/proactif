		<h1>THE ABCS OF SECURITY</h1>
		<h3>Security measures</h3>
		<p>National Bank Insurance takes measures to combat online fraud that meet industry standards. In addition to the usual precautions, it uses various sophisticated systems and methods to safeguard the confidentiality of your personal information when you carry out transactions through the ProActive, Personal Injury Insurance website.</p>
		<h3>24/7 monitoring systems</h3>
		<p>Our detection systems and advanced techniques run day and night, so that action can be taken quickly if a suspicious or unusual situation is detected.</p>
		<h3>Security and fraud prevention team</h3>
		<p>A security and fraud prevention team works tirelessly to protect your electronic transactions and establish anti-fraud systems and techniques.</p>
		<h3>128-bit transaction encryption</h3>
		<p>A 128-bit transaction encryption system provides suitable protection when sending confidential data over the Internet.</p>
		<p>We also remind you to take the necessary steps to ensure that your equipment (personal computer, mobile phone, etc.) is secure when you are carrying out transactions on the Internet. Be sure to read useful tips in the Protect yourself from fraud section.</p>
		<hr />
		<h1>Main threats</h1>
		<h2>Phishing (e-mail scams)</h2>
		<h3>Understand it</h3>
		<p>Phishing is a technique where dishonest individuals send out e-mails to try to obtain personal information about people. Phishing scams can also be sent via text message; such scams are known as SMS phishing or smishing. The same recommendations apply as for e-mail phishing.</p>
		<h3>Recognize it</h3>
		<p>Users of online commercial sites, payment organization sites and financial institution sites may all be the target of this practice. The messages contained in scam e-mail look like they originate from recognized sources. Moreover, the websites used by phishers are generally very good at imitating legitimate sites whose logos users readily recognize.</p>
		<hr />
		<h2>Protect yourself</h2>
		<h3>Do not reply to these e-mails or text messages.</h3>
		<p>Be careful: if you receive an e-mail or text message that seems to come from National Bank or MasterCard asking you to provide personal information or notifying you that you will be receiving a transfer of funds <strong>from someone you do not know</strong>, delete it. It’s probably a scam.</p>
		<h3>Never click on hyperlinks</h3>
		<p>Whether it’s an e-mail referring to contest winnings or any other alleged urgent reason, never click on hyperlinks, and don’t copy and paste them into your browser.</p>
		<h3>Don’t provide the requested information for any reason</h3>
		<p>National Bank Insurance never use this method to ask for your personal or banking information, such as credit card numbers, passwords or your mother’s maiden name.</p>
		<h3>Secure your equipment</h3>
		<p>Remember that it is essential for you to protect your computer by installing suitable protection software, and keeping it up to date automatically, including anti-virus, anti-spyware and firewall software, so as to bank with confidence.</p>
		<hr />
		<h2>Malware</h2>
		<h3>Understand it</h3>
		<p>Malware means a program or software designed to enable unauthorized persons to access a system to disrupt its operations or to change or steal data.</p>
		<h3>Recognize it</h3>
		<p>Malware can be difficult to detect, as it generally does not appear on the list of installed programs. It can be installed inadvertently and cause damage without our knowledge, as it is similar in appearance to pages we are used to viewing. It can be installed inadvertently by downloading certain free software in which it resides, by visiting certain websites or via peer-to-peer file-sharing.</p>
		<p>You may have malware on your system if:</p>
		<ul>
			<li>Your equipment is performing more slowly than usual or seems less responsive.</li>
			<li>You are automatically redirected to a page whose address you did not enter into your browser.</li>
			<li>You are constantly bombarded with pop-up advertisements.</li>
			<li>New tools or icons suddenly appear in your Internet browser, or the settings for your homepage change.</li>
			<li>Your equipment crashes or freezes more frequently.</li>
			<li>For a mobile device, you may notice unusual text messages or a sudden drop in the lifespan of your battery.</li>
		</ul>
		<h3>How to protect yourself</h3>
		<p>Malware is practically invisible, and can be reactivated at any time. To prevent any problems:</p>
		<ul>
			<li>Avoid downloading any documents or applications from unfamiliar or suspicious sources.</li>
			<li>Only do business with trusted sites.</li>
			<li>Install anti-virus and anti-spyware software.</li>
			<li>Install a firewall.</li>
			<li>Run the automatic update and clean up features of your computer.</li>
			<li>Regularly verify bills from your telecommunications supplier to detect any unusual activity.</li>
		</ul>
		<hr />
		<h2>Identity theft</h2>
		<h3>Understand it</h3>
		<p>Identity theft is on the rise. Unlike other types of fraud, identity theft can occur whenever a criminal gains access to a person’s means of identification, regardless of whether the victim is aware of that access. This information could be obtained if the victim's wallet is stolen or by using malware to unlawfully capture information on a computer, smartphone, or another device.</p>
		<h3>Recognize it</h3>
		<ul>
			<li>You stop receiving certain account statements in the mail.</li>
			<li>You now have trouble accessing credit.</li>
			<li>You receive bills for purchases you didn’t make.</li>
			<li>You receive visits or calls from collection agencies demanding payments from you.</li>
		</ul>
		<h3>How to protect yourself</h3>
		<ul>
			<li>When choosing a password, avoid using information such as your date of birth or the last digits of your social insurance number (SIN) or phone number, as these can be easily discovered.</li>
			<li>Never save passwords or personal information on your equipment.</li>
			<li>Only give out your SIN if you have no other choice. Whenever possible, provide another piece of ID.</li>
			<li>Do not carry your social insurance card on you. Instead, keep it in a safe place.</li>
			<li>Before providing any personal information, find out how it will be used, and whether it will be shared with any third parties.</li>
			<li>Activate the automatic locking function on your equipment, so that you have to enter a password to use it.</li>
			<li>Pay attention to your billing cycles. Follow up with creditors if your bills don’t arrive on time.</li>
			<li>Guard your mail. Deposit outgoing mail in postal boxes or at a post office. Promptly remove incoming mail from your mailbox after delivery. If you move or change your mailing address, be sure to have your mail forwarded.</li>
			<li>Use passwords for your credit cards, your bank accounts and your electronic devices (cell phone, electronic tablet, computer).</li>
			<li>Carry as few cards and pieces of ID on you as possible.</li>
			<li>Keep documents with personal information in a safe place. An identity thief will not hesitate to pick through your garbage or recycling bin. Be sure to shred your copies of credit card applications, insurance forms, medical reports and credit card offers you get in the mail.</li>
			<li>Order a copy of your credit file regularly from Équifax or Transunion.</li>
		</ul>
		<hr />
		<h2>Good practices</h2>
		<p>Knowing and following safe practices when doing business on the Internet is essential. Your ability to recognize, outsmart and report scams on the Web strengthens our partnership to thwart online fraud.</p>
		<h3>Manage your password and secret questions carefully</h3>
		<p>Your password is a simple and effective way to control access to your confidential information. Choosing a secure password and using it carefully will help ensure your personal information remains secure. Here are the main areas you must pay attention to in managing your password.</p>
		<h3>Choose your password carefully</h3>
		<p>Try to choose a password that will be easy for you to remember, but hard for anyone else to guess. For example, you can:</p>
		<ul>
			<li>Take the first letter of each word in a sentence about you and add numbers. <em>("I’m going on vacation on April 27" becomes "Igovo427")</em></li>
			<li>Join a few short words that make up a common, easy-to-memorize phrase <em>("milk1cow")</em></li>
			<li>Change the spelling of words <em>("120burds")</em></li>
			<li>Use the first letter or part of each word in an expression, song title or proverb. <em>("Early to bed, early to rise" becomes "E2bede2r")</em></li>
		</ul>
		<h3>Follow the required format</h3>
		<p>Your password should be made up of 8 alphanumeric characters (a combination of letters and numbers). For an even more secure password, combine upper case and lower case letters.</p>
		<h3>Avoid choosing a password based on personal information</h3>
		<p>Avoid using your first or last name, the names of your spouse or children, your date of birth, social insurance number, phone number, licence plate, name of a sports team, bank access codes, etc. Although easier to memorize, these passwords are more likely to be discovered by malicious individuals.</p>
		<h3>Do not write your password down</h3>
		<p>If you absolutely have to, write it in such a way that only you will be able to recognize it, and keep it in a safe place.</p>
		<hr />
		<h2>Do not save your password on your equipment</h2>
		<h3>Never tell anyone your password</h3>
		<p>This rule applies even to members of your family and anyone claiming to be a National Bank employee.</p>
		<p>Remember, your password is like the key to your house. You must take all the necessary precautions to prevent it from falling into someone else's hands.</p>
		<h3>Clear your browser cache</h3>
		<p>It is very important to clear the cache of your browser to safeguard the confidentiality of your financial information and improve your Internet browser’s performance.</p>
		<ul>
			<li>
				<h3>Internet Explorer 8+</h3>
				<ol>
					<li>In the <strong>Tools</strong> menu, select <strong>Delete Browsing History</strong>.</li>
					<li>If you don't see the menu, press Alt on your keyboard to show menus.</li>
					<li>Check <strong>Temporary Internet Files and Cookies</strong>.</li>
					<li>Click <strong>Delete</strong>.</li>
					<li>Click <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Internet Explorer 7.0</h3>
				<ol>
					<li>From the <strong>Tools</strong> menu, select <strong>Internet Options</strong>.</li>
					<li>On the <strong>General</strong> tab, In the <strong>History</strong>section, Click <strong>Delete</strong>... .</li>
					<li>In the <strong>Temporary Internet Files</strong> section, click <strong>Delete Files</strong>.</li>
					<li>Click <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Internet Explorer 6.x</h3>
				<ol>
					<li>From the <strong>Tools</strong> menu, select <strong>Internet Options</strong>.</li>
					<li>On the <strong>General</strong> tab, in the <strong>Temporary Internet Files</strong>section.</li>
					<li>Click <strong>Delete Files</strong>, Click <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Firefox 3.5 and higher</h3>
				<ol>
					<li>From the <strong>Tools</strong> menu, select <strong>Clear Recent History</strong>... .</li>
					<li>Click on <strong>Details</strong>.</li>
					<li>Check <strong>Cache</strong> and <strong>Cookies</strong> only.</li>
					<li>Click on <strong>Clear Now</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Firefox 1.5 to 3</h3>
				<ol>
					<li>From the <strong>Tools</strong> menu, select <strong>Clear My Tracks</strong>.</li>
					<li>Check <strong>Cache</strong> and <strong>Cookies</strong> only.</li>
					<li>Click <strong>Clear My Track Now</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Safari</h3>
				<ol>
					<li>From the Safari menu, select <strong>Empty Cache</strong>.</li>
					<li>Click <strong>Empty</strong>.</li>
				</ol>
			</li>
		</ul>
		<h3>Download the latest version of your Web browser</h3>
		<p>Downloading available updates for your browser and operating system will make sure your Web-browsing experience is secure, as you will benefit from patches, which improve your software’s effectiveness at protecting you from a variety of common threats and security vulnerabilities.</p>
		<h3>Secure your wireless network</h3>
		<p>Change your wireless network’s default settings to create an identifier and password that would be difficult for criminals to guess.</p>
		<p>Choose a strong encryption protocol, such as WPA or WPA2, that will adequately protect your network against possible intrusions.</p>
		<p>Using a public Wi-Fi network, If possible, use your mobile device’s 3G network, rather than a public Wi-Fi network.</p>
		<h3>Follow the basic rules for doing business safely</h3>
		<ul>
			<li>Do not remain connected to the Internet unnecessarily.</li>
			<li>Never leave information displayed on your screen if you leave your computer, and do not leave your equipment unattended. Always lock your computer or mobile device when it is unattended.</li>
			<li>Don’t download or install freeware, shareware or demo software from an unknown source or questionable site. Only use authorized and official applications from recognized suppliers on your mobile device; the operating system must be legitimate and must not be altered (it must not be jailbroken, rooted or pirated).</li>
			<li>Never run an executable (.exe) file received by e-mail unless it is from a known and trusted source.</li>
			<li>Be careful what you throw in the recycling bin. Shred all documents that contain your financial information, such as statements, credit card offers, receipts or insurance forms, to prevent identity theft.</li>
		</ul>
