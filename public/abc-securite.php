		<h1>L’ABC de la sécurité</h1>
		<h3>Mesures de sécurité</h3>
		<p>Banque Nationale Assurances prend des mesures conformes aux normes de l'industrie financière pour contrer la fraude en ligne. En plus des précautions habituelles, elle utilise différents systèmes et méthodes perfectionnés afin d'assurer la confidentialité de vos renseignements personnels sur le site ProActif, Assurance en cas de blessure.</p>
		<h3>Systèmes de surveillance 24/7</h3>
		<p>Utilisation de systèmes de détection et de techniques avancées en opération jour et nuit, qui permettent d'agir rapidement si une situation suspecte ou inhabituelle est détectée.</p>
		<h3>Équipe de spécialistes en sécurité et en prévention de la fraude</h3>
		<p>Une équipe en sécurité et en prévention de la fraude travaille assidûment à la protection de vos transactions électroniques et à la mise en œuvre de techniques et de systèmes de lutte contre la fraude</p>
		<h3>Chiffrement des transactions à 128 bits</h3>
		<p>Système de chiffrement des transactions à 128 bits, qui fournit une protection adéquate lors de la transmission de données confidentielles sur Internet.</p>
		<p>Nous vous rappelons aussi de prendre les mesures nécessaires pour assurer la sécurité de votre environnement informatique lorsque vous effectuez des opérations sur Internet. À cet effet, vous trouverez des conseils utiles dans la section Se protéger contre la fraude.</p>
		<hr />
		<h1>Se protéger contre la fraude</h1>
		<h2>L’hameçonnage (courriels frauduleux)</h2>
		<h3>Comprendre</h3>
		<p>L'hameçonnage, mieux connu sous l'appellation anglaise phishing, est une technique utilisant le courriel et par laquelle des individus malveillants tentent d'obtenir de l'information personnelle sur les gens. 
Le même principe existe avec les messages textes. Dans ce cas, il s’agit d’arnaque par SMS ou de « smishing » en anglais. Les recommandations sont les mêmes que pour l’hameçonnage.</p>
		<h3>Reconnaître</h3>
		<p>Autant des clients des sites d'achats en ligne, des organismes de paiements que des institutions financières peuvent être la cible de ce genre de pratique. Les messages contenus dans les courriels frauduleux semblent provenir de sources connues. Également, les sites Web qu'utilisent les hameçonneurs imitent généralement très bien les sites officiels, les logos étant déjà bien connus des utilisateurs.</p>
		<hr />
		<h2>Comment se protéger</h2>
		<h3>Ne répondez pas à ces courriels ou messages textes.</h3>
		<p>Soyez vigilant : si vous recevez un courriel ou un message texte semblant provenir de Banque Nationale Assurances vous demandant de fournir des renseignements personnels ou vous avisant que vous allez recevoir un transfert de fonds <strong>de la part d'une personne que vous ne connaissez pas</strong>, supprimez-le, car il est probablement frauduleux.</p>
		<h3>Ne cliquez jamais sur les hyperliens.</h3>
		<p>Qu'il s'agisse d'un courriel faisant référence à une offre gagnante de concours ou à tout autre prétexte urgent, ne cliquez jamais sur les hyperliens et ne les « copiez/collez » pas dans votre fureteur.</p>
		<h3>Ne fournissez l'information demandée sous aucun prétexte.</h3>
		<p>Soyez assuré que Banque Nationale Assurances n'utilise pas cette façon de faire pour vous demander vos coordonnées bancaires ou personnelles, comme un numéro de carte de crédit, un mot de passe ou le nom de jeune fille de votre mère.</p>
		<h3>Sécurisez votre équipement</h3>
		<p>N’oubliez pas qu’il est primordial de sécuriser votre poste informatique en y installant et en y maintenant à jour, de façon automatique, les logiciels de protection adéquats, entre autres, un anti-virus, un anti- espiogiciel (anti-spyware) et un pare-feu (firewall), afin de transiger en toute tranquillité.</p>
		<hr />
		<h2>Programmes malveillants</h2>
		<h3>Comprendre</h3>
		<p>Un programme malveillant est un programme ou logiciel conçu pour être implanté dans un système avec l'intention d'y accéder sans autorisation et d'en perturber le fonctionnement ou d'en altérer ou en dérober des données.</p>
		<h3>Reconnaître</h3>
		<p>Un programme malveillant est difficile à détecter, car généralement, il n’apparait pas dans la liste des programmes installés. On peut notamment « l’attraper » lors du téléchargement de logiciels gratuits auxquels il est intégré, en visitant certains sites Web et par le partage de fichiers en ligne (« peer to peer »).</p>
		<p>Vous pouvez avoir un doute sur la présence de programmes malveillants dans votre système lorsque :</p>
		<ul>
			<li>Votre ordinateur fonctionne au ralenti et semble moins performant.</li>
			<li>Vous êtes redirigé automatiquement vers une page dont vous n’avez pas tapé l’adresse dans votre Fureteur.</li>
			<li>Vous êtes sans cesse assailli de fenêtres publicitaires.</li>
			<li>Vous constatez l’apparition soudaine de nouveaux outils ou icônes dans votre fureteur Internet ou un changement dans le réglage de votre page d’accueil.</li>
			<li>Votre ordinateur est en panne ou se fige plus souvent.</li>
			<li>Pour un appareil mobile, vous pourriez constater des messages textes inhabituels ou une baisse soudaine de la durée de vie de votre batterie.</li>
		</ul>
		<h3>Comment se protéger</h3>
		<p>Les programmes malveillants sont à toute fin pratique invisible et peuvent se réactiver à tout moment. Pour éviter les problèmes :</p>
		<ul>
			<li>Ne téléchargez pas de documents ou d’applications qui proviennent de sources inconnues ou douteuses.</li>
			<li>Transigez uniquement sur des sites de confiance.</li>
			<li>Installez un anti-virus et un anti-espiogiciel.</li>
			<li>Installez un pare-feu (firewall).</li>
			<li>Exécutez la mise à jour et le nettoyage de votre poste de façon automatique.</li>
			<li>Vérifiez régulièrement vos factures de votre fournisseur de télécommunications pour y déceler toutes activités inhabituelles.</li>
		</ul>
		<hr />
		<h2>Vol d’identité</h2>
		<h3>Comprendre</h3>
		<p>L'usurpation d'identité est de plus en plus fréquente. Contrairement aux autres types de fraudes, elle devient possible lorsqu'un criminel obtient l'accès aux moyens d'identification d'une personne, que celle-ci en soit consciente ou non. Cette information peut être obtenue, entre autres, à la suite d’un vol de porte-monnaie ou par capture d’information illicite sur un ordinateur, téléphone intelligent ou autre équipement à l’aide d’un programme malveillant.</p>
		<h3>Reconnaître</h3>
		<ul>
			<li>Vous ne recevez plus certains relevés de compte par la poste.</li>
			<li>Vous avez maintenant de la difficulté à obtenir du crédit.</li>
			<li>Vous recevez des factures pour des achats que vous n’avez pas faits.</li>
			<li>Vous recevez la visite ou les appels de compagnies de recouvrement, qui vous réclament des montants d’argent.</li>
		</ul>
		<h3>Comment se protéger</h3>
		<ul>
			<li>Lorsque vous choisissez un mot de passe, évitez, par exemple, de prendre votre date de naissance ou les derniers chiffres de vos numéros d'assurance sociale (NAS) et de téléphone, car il peut être relativement facile de se les procurer.</li>
			<li>Ne sauvegardez pas votre mot de passe, ni vos renseignements personnels dans les fichiers de vos équipements.</li>
			<li>Ne donnez votre NAS que si vous ne pouvez faire autrement. Dans la mesure du possible, proposez une autre pièce d'identité.</li>
			<li>Ne traînez pas votre carte d'assurance sociale avec vous. Rangez-la plutôt dans un endroit sûr.</li>
			<li>Avant de donner des renseignements personnels, informez-vous de la façon dont ils seront utilisés et s'ils seront communiqués à des tiers.</li>
			<li>Activez la fonction de verrouillage automatique de vos équipements qui requiert l’entrée d’un mot de passe pour retourner à votre tâche.</li>
			<li>Surveillez le cycle de votre facturation. Si vous ne recevez pas vos factures au moment prévu, avisez-en vos créanciers.</li>
			<li>Veillez sur votre courrier. Déposez-le dans les boîtes de collecte prévues à cette fin ou à votre bureau de poste local. Également, ne tardez pas à retirer le courrier reçu de votre boîte aux lettres. Assurez-vous de faire suivre votre courrier si vous déménagez ou changez d'adresse postale.</li>
			<li>Utilisez des mots de passe pour vos cartes de crédit, vos comptes de banque et vos équipements (téléphone mobile, tablette électronique, ordinateur).</li>
			<li>Ayez en mains avec vous un minimum de pièces d'identité et de cartes.</li>
			<li>Gardez les documents contenant vos renseignements personnels dans un endroit sûr. Un usurpateur d'identité ne se gênera pas pour fouiller dans vos poubelles et dans votre bac de recyclage. Assurez-vous de déchiqueter vos copies de demandes de carte de crédit, vos formulaires d'assurance, vos rapports médicaux et les offres de carte de crédit reçues par courrier.</li>
			<li>Obtenez régulièrement une copie de votre dossier de crédit auprès d’Équifax ou de Transunion.</li>
		</ul>
		<hr />
		<h2>Bonnes pratiques</h2>
		<p>Connaître et appliquer des pratiques sécuritaires lorsque vous transigez sur Internet est essentiel. Votre habileté à reconnaître, à déjouer et à signaler les escroqueries sur le Web renforce le partenariat que nous formons pour faire échec à la fraude en ligne.</p>
		<h3>Gérez bien votre mot de passe et vos questions secrètes</h3>
		<p>Votre mot de passe est une façon simple et efficace de contrôler l’accès à vos données confidentielles. Bien le choisir et en faire un emploi prudent, c’est maximiser votre sécurité. Voici les principaux éléments à considérer dans la gestion de votre mot de passe</p>
		<h3>Choisissez votre mot de passe avec soin</h3>
		<p>L’objectif est de trouver un mot de passe facile à retenir, mais difficile à deviner. Pour y arriver, vous pouvez utiliser l’un des trucs suivants :</p>
		<ul>
			<li>Prenez les premières lettres de chaque mot d’une expression ou d’une phrase vous concernant personnellement et joignez-y des chiffres. Ainsi, <em>Je pars en vacances ce 27 avril</em> peut devenir <em>Jpevc274.</em></li>
			<li>Réunissez deux ou trois mots courts qui forment une phrase courante facile à mémoriser, comme <em>Jai1chat.</em></li>
			<li>Changez l’orthographe des mots choisis, comme <em>120oisos</em></li>
			<li>Utilisez les premières lettres ou une partie de chaque mot d’une expression, d’une chanson ou d’un proverbe. Par exemple, <em>1tvmq2tl</em> peut être l’abréviation de <em>Un tiens vaut mieux que deux tu l’auras.</em></li>
		</ul>
		<h3>Respectez le format exigé</h3>
		<p>Votre mot de passe doit se composer de huit caractères alphanumériques de votre choix, soit une combinaison obligatoire de chiffres et de lettres. L’utilisation de majuscules et de minuscules en augmentera l’efficacité</p>
		<h3>Évitez de choisir un mot de passe basé sur vos renseignements personnels</h3>
		<p>Évitez d’utiliser vos nom et prénom, ceux de votre conjoint ou de vos enfants, votre date de naissance, vos numéros d’assurance sociale, de téléphone, d’immatriculation ou d’une carte, le nom d’une équipe sportive, votre code d’accès bancaire, etc. Bien que ces mots soient souvent faciles à mémoriser, il y a plus de risques qu’ils soient découverts par des gens malintentionnés.</p>
		<h3>N’inscrivez pas votre mot de passe sur papier</h3>
		<p>Si vous choisissez tout de même de le faire, vous devez absolument l’inscrire sous une forme que vous seul pouvez reconnaître et le garder dans un endroit sûr.</p>
		<hr />
		<h2>Ne sauvegardez pas votre mot de passe sur votre équipement</h2>
		<h3>Ne divulguez jamais votre mot de passe</h3>
		<p>Cette règle s’applique même aux membres de votre famille et à quiconque se présente comme un employé de Banque Nationale Assurances.</p>
		<p>Souvenez-vous que votre mot de passe est comme la clé de votre maison. Il vous faut donc prendre les précautions qui s'imposent pour qu'il ne tombe pas entre les mains d'une autre personne.</p>
		<h3>Effacez votre mémoire cache</h3>
		<p>Il est très important d'effacer la mémoire cache de votre fureteur. Ceci a pour but de protéger la confidentialité de vos renseignements financiers et d'accroître la performance de votre fureteur Internet.</p>
		<ul>
			<li>
				<h3>Internet Explorer 8+</h3>
				<ol>
					<li>Dans le menu <strong>Outils</strong>, sélectionner <strong>Supprimer l'historique de navigation</strong>.</li>
					<li>Si le menu n'apparaît pas, appuyez sur la touche Alt de votre clavier pour afficher les menus.</li>
					<li>Cochez <strong>Supprimer l’historique de navigation et Cookies</strong>.</li>
					<li>Cliquer sur <strong>Supprimer</strong>.</li>
					<li>Cliquer sur <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Internet Explorer 7.0</h3>
				<ol>
					<li>Dans le menu <strong>Outils</strong>, sélectionner <strong>Options Internet</strong>.</li>
					<li>Dans l’onglet <strong>Général</strong>, section <strong>Historique de navigation</strong>, Cliquer sur <strong>Supprimer</strong>... .</li>
					<li>Dans la section <strong>Fichiers Internet temporaires</strong>, cliquer sur <strong>Supprimer les fichiers</strong>.</li>
					<li>Cliquer sur <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Internet Explorer 6.x</h3>
				<ol>
					<li>Dans le menu <strong>Outils</strong>, sélectionner <strong>Options Internet</strong>.</li>
					<li>Dans l’onglet <strong>Général</strong>, section <strong>Fichiers temporaires Internet</strong>,</li>
					<li>Cliquer sur <strong>Supprimer les fichiers</strong> puis <strong>OK</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Firefox 3.5 et plus</h3>
				<ol>
					<li>Dans le menu <strong>Outils</strong>, sélectionner <strong>Supprimer l'historique récent</strong>... .</li>
					<li>Cliquer sur <strong>Détails</strong>.</li>
					<li>Cocher seulement <strong>Cookies</strong> et <strong>Cache</strong>.</li>
					<li>Cliquer sur <strong>Effacer maintenant</strong>.</li>
				</ol>
			</li>
			<li>
				<h3>Firefox 1.5 à 3</h3>
				<ol>
					<li>Dans le menu Outils, sélectionner Effacer mes traces.</li>
					<li>Cocher seulement Cookies et Cache.</li>
					<li>Cliquer sur Effacer mes traces maintenant.</li>
				</ol>
			</li>
			<li>
				<h3>Safari</h3>
				<ol>
					<li>Dans le menu Safari, sélectionner <strong>Vider le cache</strong>.</li>
					<li>Cliquer sur <strong>Vider</strong>.</li>
				</ol>
			</li>
		</ul>
		<h3>Téléchargez la dernière version de votre fureteur Web</h3>
		<p>Téléchargez les mises à jour disponibles pour votre fureteur et votre système d’exploitation vous assurera une navigation Web sécuritaire, puisque vous profiterez des rustines, qui renforceront l’efficacité de votre logiciel de protection contre les différentes menaces informatiques et les failles de sécurité (vulnérabilités) les plus courantes.</p>
		<h3>Sécurisez votre réseau sans fil</h3>
		<p>Modifiez les paramètres par défaut de votre réseau sans fil afin de créer un mot de passe et un identifiant difficiles à deviner pour d’éventuels fraudeurs.</p>
		<p>Privilégiez un chiffrement fort, tel que le chiffrement en WPA ou WPA2, afin de protéger adéquatement votre réseau contre de possibles intrusions.</p>
		<p>Utilisation d’un réseau sans-fil public Utilisez de préférence le réseau 3G de votre appareil mobile, plutôt qu’un réseau sans-fil (WI-FI) public</p>
		<h3>Suivez les règles de base pour transiger en toute sécurité</h3>
		<ul>
			<li>Ne laissez pas inutilement votre équipement branché au réseau Internet.</li>
			<li>Ne laissez pas sans surveillance votre équipement, ni l'information affichée sur votre écran. Prenez l'habitude de verrouiller l'accès aux données de votre équipement.</li>
			<li>Évitez de télécharger ou d'installer des logiciels gratuits, prêtés ou en évaluation et dont vous ne connaissez pas la provenance. Il en est de même pour les logiciels provenant de sites douteux. De même, assurez-vous d’utiliser des applications autorisées ou officielles provenant de fournisseurs reconnus sur votre appareil mobile. Son système d’exploitation doit être légitime et non altéré (non « jailbreaké », non « rooté », non piraté).</li>
			<li>N'ouvrez jamais un fichier exécutable (.exe) si sa provenance vous est inconnue ou non fiable.</li>
			<li>Faites attention à ce que vous mettez au recyclage. Déchiquetez les documents où figurent vos renseignements financiers, tels les relevés, les offres de carte de crédit, les reçus et les formulaires d'assurance, pour éviter le vol de votre identité.</li>
		</ul>
